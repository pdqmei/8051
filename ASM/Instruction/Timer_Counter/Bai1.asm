;Nhấp nháy led tạo độ trễ bằng timer 

ORG 0000H

MAIN:
	SETB P2.5 ; OFF LED

LOOP:
	CPL P2.5
	CALL DELAY_1S
	SJMP LOOP

DELAY_1S:
	MOV R7, #100
	
DELAY_LOOP:
	CALL DELAY_10MS
	DJNZ R7, DELAY_LOOP
	RET
	
DELAY_10MS:
	MOV TMOD, #01H
	MOV TH0, #0D8H
	MOV TL0, #0F0H
	SETB TR0

WAIT:
	JNB TF0, WAIT
	CLR TR0
	CLR TF0
	RET

END
